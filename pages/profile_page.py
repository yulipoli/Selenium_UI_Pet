    def edit_pet(self):
        edit_btn = self.browser.find_element(*ProfilePageLocators.EDIT_BTN)
        edit_btn.click()
        edit_name = self.browser.find_element(*ProfilePageLocators.EDIT_NAME)
        edit_name.clear()
        edit_name.send_keys("NewPet")
        time.sleep(2)
        edit_age = self.browser.find_element(*ProfilePageLocators.EDIT_AGE)
        edit_age.clear()
        edit_age.send_keys('1')
        edit_save_btn = self.browser.find_element(*ProfilePageLocators.EDIT_SAVE_BTN)
        edit_save_btn.click()
        time.sleep(2)

    def delete_pet(self):
        delete_btn = self.browser.find_element(*ProfilePageLocators.DELETE_BTN)
        time.sleep(5)
        delete_btn.click()
        time.sleep(5)
        yes_btn = self.browser.find_element(*ProfilePageLocators.YES_BTN)
        time.sleep(5)
        yes_btn.click()
        time.sleep(5)
