        create_age.send_keys('5')
        create_type = self.browser.find_element(*ProfilePageLocators.CREATE_TYPE)
        create_type.click()
        type_cat = self.browser.find_element(*ProfilePageLocators.TYPE_CAT)
        type_cat.click()
        create_gender = self.browser.find_element(*ProfilePageLocators.CREATE_GENDER)
        create_gender.click()
        gender_female = self.browser.find_element(*ProfilePageLocators.GENDER_FEMALE)
        gender_female.click()
        submit_btn = self.browser.find_element(*ProfilePageLocators.SUBMIT_BTN)
        submit_btn.click()
        choose_btn = self.browser.find_element(*ProfilePageLocators.CHOOSE_BTN)
        choose_btn.click()
